package opAritimeticos;

public class OperadoresAritmeticos {
	
	public static void main(System[] args) {
		int numeroXis = 3;
		int numeroYplo = 2;
		double qualquer = 0;
		
		qualquer = numeroXis + numeroYplo; //adicao
		System.out.println("É: \n"+qualquer);
		
		qualquer = numeroXis - numeroYplo; //subtracao
		System.out.println("É: \n"+qualquer);
		
		qualquer = numeroXis * numeroYplo; //multiplicacao
		System.out.println("É: \n"+qualquer);
		
		qualquer = numeroXis / numeroYplo; //divisao
		System.out.println("É: \n"+qualquer);
		
		qualquer = numeroXis % numeroYplo; //modulo
		System.out.println("É: \n"+qualquer);
		
		
	}

}
